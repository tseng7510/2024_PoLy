          }
        }]
      });
    });

    (function() {
      let videoBtn = document.querySelector('.videoBtn')
      let body = document.querySelector('body')
      let videoList = document.querySelector('.videoList .listBox')
      let lightBoxClose = document.querySelector('.lightBoxClose')

      let videoUrl = document.querySelectorAll('.videoList .item');
      let videoBox = document.querySelector('.videoBox .videoContainer');

      videoUrl.forEach((item) => {
        let checkUrl = item.dataset.url;

        item.addEventListener('click', function() {
          videoBox.innerHTML = checkUrl
        })
      })

      videoBtn.addEventListener('click', function(e) {
        e.preventDefault();
        body.classList.toggle('lightBoxActive');
        videoList.slick.refresh();
        videoBox.innerHTML = videoUrl[0].dataset.url
      })
      lightBoxClose.addEventListener('click', function(e) {
        body.classList.toggle('lightBoxActive');
        videoBox.innerHTML = ''
      })

    })();
